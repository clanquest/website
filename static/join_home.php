	<h2>Our Meetups</h2>

	<img src="/forums/styles/clanquest/images/join_brochure/cq_irl.jpg" srcset="/forums/styles/clanquest/images/join_brochure/cq_irl_2x.jpg 2x" title="Our Meetups in Person" alt="A collage of our meetups in person">

	<p>While Clan Questers obviously play video games together, and use our forums and <a href="https://discord.clanquest.org" target="_blank">Discord server</a> to 
	communicate, real life meetups have become one of our community’s coolest activities. With our 
	continued growth, we have hosted meetings on four continents, and continue to aspire to be 
	a leader in the online community.</p>

	<p>Whether it’s a small meetup in Atlanta, attending a game convention like RuneFest, or 
	larger gatherings like the clan’s own Quest Fest, meetups have become regular occurrences. 
	Friends from online are becoming friends in person, and while not mandatory, many of us 
	have found meetups are a joy to attend. Friendships made in Clan Quest can expand to the real world if you desire.</p>

